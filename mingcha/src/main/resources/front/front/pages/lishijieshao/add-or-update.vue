        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let chayeTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'chaye_types',
            }
			let chayeTypes = await this.$api.page(`dictionary`, chayeTypesParams);
			this.chayeTypesOptions = chayeTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`lishijieshao`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.lishijieshaoId){
                this.ruleForm.lishijieshaoId = options.lishijieshaoId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            chayeTypesChange(e) {
                this.chayeTypesIndex = e.target.value
                this.ruleForm.chayeValue = this.chayeTypesOptions[this.chayeTypesIndex].indexName
                this.ruleForm.chayeTypes = this.chayeTypesOptions[this.chayeTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			lishijieshaoPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.lishijieshaoPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			lishijieshaoFileTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.lishijieshaoFile = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			qiyuanTimeConfirm(val) {
                this.ruleForm.qiyuanTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.lishijieshaoUuidNumber)) {
                    this.$utils.msg(`历史编号不能为空`);
                    return
                }
				if ((!this.ruleForm.lishijieshaoName)) {
                    this.$utils.msg(`茗茶历史名称不能为空`);
                    return
                }
				if ((!this.ruleForm.lishijieshaoPhoto)) {
                    this.$utils.msg(`封面不能为空`);
                    return
                }
				if ((!this.ruleForm.chayeTypes)) {
                    this.$utils.msg(`茶叶类型不能为空`);
                    return
                }
				if ((!this.ruleForm.lishijieshaoQiyuandi)) {
                    this.$utils.msg(`起源地不能为空`);
                    return
                }
				if ((!this.ruleForm.qiyuanTime)) {
                    this.$utils.msg(`起源时间不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`lishijieshao`, this.ruleForm);
                } else {
                    await this.$api.save(`lishijieshao`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
